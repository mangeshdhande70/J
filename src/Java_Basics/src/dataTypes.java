public class dataTypes {

    public static void main(String[] args) {


        float res = 12 + 12.5f;
        System.out.println(res);

       // int res1 = 12 + 12.5f;

        float res2 = 12.5f + 12.5f;
        System.out.println(res2);

        double res3 = 12.4f + 12.5f;
        System.out.println(res3);

        // float res4 = 12.5 + 12.5 ; Not possible bcz double is greater in size

        int res4 = 'A' + 45;   // A= 65 , 65+45 = 110
        System.out.println(res4);

//        int res5 = A + 45;
//        System.out.println(res5);

        short res6 = 6+5;
        System.out.println(res6);

       //  int res7 = 45L + 45L;  Not possible bcz Lone is greater in size

        long res7 = 45L + 45L;
        System.out.println(res7);

        long res8 = 45 + 45;
        System.out.println(res8);
    }
}
