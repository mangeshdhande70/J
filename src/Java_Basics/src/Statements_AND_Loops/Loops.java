package Statements_AND_Loops;

public class Loops {

    public static void main(String[] args) {

        int i = 1;
        while (i<=5)
        {
            System.out.print("* " );
            i++;
        }
        System.out.println("\n"+i);


        int j=5;
        while (j>=1)
        {
            System.out.print(j+" " );
            j--;
        }

        System.out.println("\n"+j);


        int a = 1;
         while(a!=5)
         {
             System.out.print(a +" ");
             a++;
         }

    }
}
